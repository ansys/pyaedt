from ansys.aedt.core.emit_core.nodes.emit_node import EmitNode
from ansys.aedt.core.internal.checks import min_aedt_version


class TouchstoneCouplingNode(EmitNode):
    def __init__(self, emit_obj, result_id, node_id) -> None:
        EmitNode.__init__(self, emit_obj, result_id, node_id)
        self._is_component = False

    @property
    @min_aedt_version("2025.2")
    def parent(self) -> EmitNode:
        """The parent of this emit node."""
        return self._parent

    @property
    @min_aedt_version("2025.2")
    def node_type(self) -> str:
        """The type of this emit node."""
        return self._node_type

    @min_aedt_version("2025.2")
    def duplicate(self, new_name: str = "") -> EmitNode:
        """Duplicate this node"""
        return self._duplicate(new_name)

    @min_aedt_version("2025.2")
    def delete(self) -> None:
        """Delete this node"""
        self._delete()

    @property
    @min_aedt_version("2025.2")
    def enabled(self) -> bool:
        """Enable/Disable coupling.

        Value should be 'true' or 'false'.
        """
        val = self._get_property("Enabled")
        return val == "true"

    @enabled.setter
    @min_aedt_version("2025.2")
    def enabled(self, value: bool) -> None:
        self._set_property("Enabled", f"{str(value).lower()}")

    @property
    @min_aedt_version("2025.2")
    def filename(self) -> str:
        """Name of file with coupling data.

        Value should be a full file path.
        """
        val = self._get_property("Filename")
        return val

    @filename.setter
    @min_aedt_version("2025.2")
    def filename(self, value: str) -> None:
        self._set_property("Filename", f"{value}")

    @property
    @min_aedt_version("2025.2")
    def savant_matched_coupling_file(self) -> str:
        """Coupling data generated by Savant and exported as a matched file."""
        val = self._get_property("Savant Matched Coupling File")
        return val

    @property
    @min_aedt_version("2025.2")
    def enable_em_isolation(self) -> bool:
        """Enables/disables EM isolation.

        Value should be 'true' or 'false'.
        """
        val = self._get_property("Enable EM Isolation")
        return val == "true"

    @enable_em_isolation.setter
    @min_aedt_version("2025.2")
    def enable_em_isolation(self, value: bool) -> None:
        self._set_property("Enable EM Isolation", f"{str(value).lower()}")

    @property
    @min_aedt_version("2025.2")
    def port_antenna_assignment(self) -> list[str]:
        """Maps each port in the coupling file to an antenna in the project."""
        val = self._get_property("Port-Antenna Assignment")
        return val

    @port_antenna_assignment.setter
    @min_aedt_version("2025.2")
    def port_antenna_assignment(self, value: list[str] | list[EmitNode] | str) -> None:
        if isinstance(value, (list, tuple)):
            if all(isinstance(v, EmitNode) for v in value):
                value = "|".join(self._full_node_name(v.name) for v in value)
            else:
                value = "|".join(self._full_node_name(v) for v in value)
        else:
            parts = value.split("|")
            value = "|".join(self._full_node_name(p) for p in parts)
        self._set_property("Port-Antenna Assignment", f"{value}")

    @property
    @min_aedt_version("2025.2")
    def notes(self) -> str:
        """Expand to view/edit notes stored with the project."""
        val = self._get_property("Notes")
        return val

    @notes.setter
    @min_aedt_version("2025.2")
    def notes(self, value: str) -> None:
        self._set_property("Notes", f"{value}")
